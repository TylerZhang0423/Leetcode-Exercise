//264. 丑数 II
//编写一个程序，找出第 n 个丑数。
//丑数就是只包含质因数 2, 3, 5 的正整数。
//示例:
//输入: n = 10
//输出: 12
//解释: 1, 2, 3, 4, 5, 6, 8, 9, 10, 12 是前 10 个丑数。
//说明:  
//1 是丑数。
//n 不超过1690。
package uglynumberII;

public class Solution {
    public int nthUglyNumber(int n) {
        int flag=0;
    	for (int i=1;;i++) {
        	if (isUgly(i)) {
        		flag++;
        	}
        	if (flag==n) {
        		return i;
        	}
        }
    }
    public boolean isUgly(int num) {
    	if (num==1) {
    		return true;
    	}
    	if (num<=0) {
    		return false;
    	}
        while (num>1||num<-1) {
        	if (num%2==0) {
        		num=num/2;
        	}else if (num%3==0) {
        		num=num/3;
        	}else if (num%5==0) {
        		num=num/5;
        	}else {
        		return false;
        	}
        }
        return true;
    }
}
