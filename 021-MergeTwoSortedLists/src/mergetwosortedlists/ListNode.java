//21. 合并两个有序链表
//将两个有序链表合并为一个新的有序链表并返回。
//新链表是通过拼接给定的两个链表的所有节点组成的。 
//示例：
//输入：1->2->4, 1->3->4
//输出：1->1->2->3->4->4
package mergetwosortedlists;

public class ListNode {
	int val;
	ListNode next;
	public ListNode getNext() {
		return next;
	}
	public void setNext(ListNode next) {
		this.next = next;
	}
	ListNode(int x) { val = x; }
}
